import fs from "fs";
import path from "path";

const directoryPath = path.join('lib', 'ts');  // Adjust the directory as needed
let exports = '';

const flatbuffersEncryptionRuntime = `// automatically generated by the FlatBuffers compiler, do not modify

/* eslint-disable @typescript-eslint/no-unused-vars, @typescript-eslint/no-explicit-any, @typescript-eslint/no-non-null-assertion */

import initHDWallet from 'hd-wallet-wasm';

/**
 * FlatBuffers field-level encryption support using AES-256-CTR.
 * Uses hd-wallet-wasm so browser and Node.js callers share the same WASM/OpenSSL path.
 */

type HDWalletModule = {
  aesCtr: {
    encrypt(key: Uint8Array, plaintext: Uint8Array, iv: Uint8Array): Uint8Array;
    decrypt(key: Uint8Array, ciphertext: Uint8Array, iv: Uint8Array): Uint8Array;
  };
};

let _wallet: Promise<HDWalletModule> | null = null;
function hdWallet(): Promise<HDWalletModule> {
  if (!_wallet) _wallet = initHDWallet() as unknown as Promise<HDWalletModule>;
  return _wallet;
}

// Derive a 16-byte counter/IV from encryption context and field offset
function deriveCounter(ctx: Uint8Array, fieldOffset: number): Uint8Array {
  if (ctx.length < 12) {
    throw new Error('Encryption context must be at least 12 bytes');
  }
  const counter = new Uint8Array(16);
  counter.set(ctx.slice(0, 12));
  const view = new DataView(counter.buffer, counter.byteOffset);
  view.setUint32(12, fieldOffset, true); // Little-endian
  return counter;
}

// Encrypt bytes using AES-256-CTR via hd-wallet-wasm
async function encryptBytes(data: Uint8Array, ctx: Uint8Array, fieldOffset: number): Promise<Uint8Array> {
  if (ctx.length < 32) {
    throw new Error('Encryption context must be at least 32 bytes');
  }
  const keyBytes = ctx.slice(0, 32);
  const counter = deriveCounter(ctx, fieldOffset);
  return (await hdWallet()).aesCtr.encrypt(keyBytes, data, counter);
}

// Decrypt bytes using AES-256-CTR via hd-wallet-wasm
async function decryptBytes(data: Uint8Array, ctx: Uint8Array, fieldOffset: number): Promise<Uint8Array> {
  if (ctx.length < 32) {
    throw new Error('Encryption context must be at least 32 bytes');
  }
  const keyBytes = ctx.slice(0, 32);
  const counter = deriveCounter(ctx, fieldOffset);
  return (await hdWallet()).aesCtr.decrypt(keyBytes, data, counter);
}

export class FlatbuffersEncryption {
  static async decryptScalarAsync<T extends number>(
    value: T,
    ctx: Uint8Array | null,
    fieldOffset: number
  ): Promise<T> {
    if (!ctx) return value;
    const buffer = new ArrayBuffer(4);
    const view = new DataView(buffer);
    view.setFloat32(0, value as number, true);
    const encrypted = new Uint8Array(buffer);
    const decrypted = await decryptBytes(encrypted, ctx, fieldOffset);
    const decryptedView = new DataView(decrypted.buffer, decrypted.byteOffset);
    return decryptedView.getFloat32(0, true) as T;
  }

  static async decryptStringAsync(
    data: Uint8Array | null,
    ctx: Uint8Array | null,
    fieldOffset: number
  ): Promise<Uint8Array | null> {
    if (!ctx || !data) return data;
    return decryptBytes(data, ctx, fieldOffset);
  }

  static async encryptBytes(
    data: Uint8Array,
    ctx: Uint8Array,
    fieldOffset: number
  ): Promise<Uint8Array> {
    return encryptBytes(data, ctx, fieldOffset);
  }

  static async decryptBytes(
    data: Uint8Array,
    ctx: Uint8Array,
    fieldOffset: number
  ): Promise<Uint8Array> {
    return decryptBytes(data, ctx, fieldOffset);
  }

  /** @deprecated Use decryptScalarAsync instead */
  static decryptScalar<T extends number>(value: T, ctx: Uint8Array | null, fieldOffset: number): T {
    if (!ctx) return value;
    console.warn('FlatbuffersEncryption.decryptScalar is deprecated. Use decryptScalarAsync.');
    return value;
  }

  /** @deprecated Use decryptStringAsync instead */
  static decryptString(data: Uint8Array | null, ctx: Uint8Array | null, fieldOffset: number): Uint8Array | null {
    if (!ctx || !data) return data;
    console.warn('FlatbuffersEncryption.decryptString is deprecated. Use decryptStringAsync.');
    return data;
  }
}

export { encryptBytes, decryptBytes, deriveCounter };
`;

function ensureFlatbuffersEncryptionRuntime() {
    for (const schemaName of ['KMF', 'REC']) {
        const folderPath = path.join(directoryPath, schemaName);
        if (!fs.existsSync(folderPath)) {
            continue;
        }
        fs.writeFileSync(path.join(folderPath, 'flatbuffers-encryption.ts'), flatbuffersEncryptionRuntime);
    }
}

function writeFolderMain(folderPath) {
    const exportLines = fs
        .readdirSync(folderPath, { withFileTypes: true })
        .filter((dirent) => dirent.isFile())
        .map((dirent) => dirent.name)
        .filter((name) => name.endsWith('.ts'))
        .filter((name) => name !== 'main.ts' && name !== 'index.ts')
        .sort((left, right) => left.localeCompare(right))
        .map((name) => `export * from './${name.replace(/\.ts$/, '.js')}';`);

    const contents = [
        '// automatically generated by the FlatBuffers compiler, do not modify',
        '',
        '/* eslint-disable @typescript-eslint/no-unused-vars, @typescript-eslint/no-explicit-any, @typescript-eslint/no-non-null-assertion */',
        '',
        ...exportLines,
        '',
    ].join('\n');

    fs.writeFileSync(path.join(folderPath, 'main.ts'), contents);
}

ensureFlatbuffersEncryptionRuntime();

// Read all directory names under the specified path
fs.readdirSync(directoryPath, { withFileTypes: true })
    .filter(dirent => dirent.isDirectory())
    .forEach(dirent => {
        const folderName = dirent.name;
        writeFolderMain(path.join(directoryPath, folderName));
        exports += `export * as ${folderName.toUpperCase()} from './${folderName}/main.js';\n`;
    });

// Write the master index.ts file
fs.writeFileSync(path.join(directoryPath, 'index.ts'), exports);
